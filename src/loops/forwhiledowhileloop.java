package loops;

public class forwhiledowhileloop {
    public static void main(String[] args) {
        //for loop
        for(int i=9;i<=7;i++){
            System.out.println(i);
        }

        //while loop
        int j=10;
        while(j<=8){
            System.out.println(j);
            j++;
        }

        //do while loop-> it runs at least one time
        int k=23;
        do{
            System.out.println(k);
            k++;
        } while(k<=9);
    }
}
