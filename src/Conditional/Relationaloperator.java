package Conditional;

public class Relationaloperator {
    public static void main(String [] args){
        int a=5;
        int b=4;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        //this all provides boolean value
    }
}
