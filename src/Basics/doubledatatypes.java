package Basics;
public class doubledatatypes {
    public static void main(String [] args){
        double x=23.5;
        float y=6.7f;
        int z=7;
        System.out.println(x+y+z);
        System.out.println(x+z);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/z);
    }
}
