package Basics;
public class variableupdation {
    public static void main(String[] args) {
        int x=6;
        System.out.println(x);
        x=x+5;//x+=5;
        System.out.println(x);
        x=x-3;//x-=3
        System.out.println(x);
        x=x*4;//x*=4
        System.out.println(x);
        x=x/4;//x/=4
        System.out.println(x);
    }
}
