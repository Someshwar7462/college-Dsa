package Basics;
public class Arithmeticoperation {
    public static void main(String [] args){
        int x=5,y=3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        // when we apply any operation on int data types then result must be integer
    }
}
